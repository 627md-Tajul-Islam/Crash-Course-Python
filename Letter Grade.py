marks = int(input("Enter Your Marks: "))

if marks >= 90 and marks <=100 :
    print("A+")

elif marks >=85 and marks <=89 :
    print("A")

elif marks >=80 and marks <=84 :
    print("A-")

elif marks >= 77 and marks <= 89 :
    print("B+")

elif marks >= 73 and marks <= 76 :
    print("B")

elif marks >= 70 and marks <= 72:
    print("B-")

else :
    print("Fail")