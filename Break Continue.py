# break keyword use korar karone loop theke direct ber hoye asbe and check korbe je condition miltese kina

i = 1
while i<=100:
  if i == 20:
    break
  print(i)
  i = i + 1
print("END OF THE PROGRAM")

# continue use korle loop theke baire jabe na but condition e back asbe.
